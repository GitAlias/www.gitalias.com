<h1 id="git-undo-commit-hard">git undo-commit-hard</h1>
<h2 id="undo-a-commit-with-a-hard-reset">Undo a commit with a hard
reset</h2>
<p>Git alias:</p>
<pre class="git"><code>undo-commit-hard = reset --hard HEAD~1</code></pre>
<p>Example:</p>
<pre class="shell"><code>git undo-commit-hard</code></pre>
