<h1 id="git-bv">git bv</h1>
<h2 id="list-branch-information-with-hash-and-subject">List branch
information with hash and subject</h2>
<p>Git alias:</p>
<pre class="git"><code>bv = branch --verbose</code></pre>
<p>Example:</p>
<pre class="shell"><code>git bv</code></pre>
<p>When in list mode, then show the hash, the commit subject line,
etc.</p>
<p>This is identical to doing <code>git b -v</code>.</p>
