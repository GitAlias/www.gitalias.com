<h3 id="why-use-this">Why use this?</h3>
<p>We are creating this alias list because we type these commands many
times daily, and we want the commands to be fast and also accurate.</p>
<p>We often work on teams, across many companies and organizations, and
using multiple shells. We want to count on a set of aliases. For shorter
commands, such as <code>s</code> for <code>status</code>, fast speed is
nice. For longer commands, such as <code>repacker</code>, accurate
settings are important.</p>

