<h1 id="introduction">Introduction</h1>
<h2 id="what-is-git-alias">What is Git Alias?</h2>
<p>Git Alias is a collection of git version control shortcuts,
functions, and commands:</p>
<ul>
<li><p>Shortcuts such as <code>s</code> for
<code>status</code>.</p></li>
<li><p>Improvements such as <code>optimize</code> to do a prune and
repack with recommended settings.</p></li>
<li><p>Workflows such as <code>topic-begin</code> to create a new topic
branch for a new feature.</p></li>
<li><p>Visualizations such as <code>graphviz</code> to show logs and
charts using third-party tools.</p></li>
</ul>
