<h1 id="move-backwards-on-the-commit-chain">Move backwards on the commit
chain</h1>
<p>Git alias:</p>
<pre class="git"><code>reset-commit-hard-clean = !git reset --hard HEAD~1 &amp;&amp; git clean -df</code></pre>
<p>Example:</p>
<pre class="shell"><code>git reset-commit-hard-clean</code></pre>
