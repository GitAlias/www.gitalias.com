<h1 id="git-smuir">git smuir</h1>
<h2
id="submodule-update-with-initialize-and-recursive-this-is-useful-to-bring-a-submodule-fully-up-to-date.">submodule
update with initialize and recursive; this is useful to bring a
submodule fully up to date.</h2>
<p>Git alias:</p>
<pre class="git"><code># git smuir:
smuir = submodule update --init --recursive</code></pre>
<p>Example:</p>
<pre class="shell"><code>git smuir foo</code></pre>
<p>This can be useful to bring a submodule fully up to date.</p>

