<h1 id="git-gn">git gn</h1>
<h2 id="grep-and-show-line-number">Grep and show line number</h2>
<p>Git alias:</p>
<pre class="git"><code>gn = grep -n</code></pre>
<p>Example:</p>
<pre class="shell"><code>git gn</code></pre>
<p>Same as <code>git grep --line-number</code>.</p>

