<h1 id="git-reset-to-upstream">git reset-to-upstream</h1>
<h1 id="reset-commits-back-to-the-upstream-branch">Reset commits back to
the upstream branch</h1>
<p>Git alias:</p>
<pre class="git"><code>reset-to-upstream = !git reset --hard $(git upstream-branch)</code></pre>
<p>Example:</p>
<pre class="shell"><code>git reset-to-upstream</code></pre>
