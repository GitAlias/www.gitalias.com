<h1 id="git-rvnc">git rvnc</h1>
<h2 id="revert-with-no-commit-i.e.-without-autocommit">Revert with no
commit i.e. without autocommit</h2>
<p>Git alias:</p>
<pre class="git"><code>rvnc = revert --no-commit</code></pre>
<p>Example:</p>
<pre class="shell"><code>git rvnc</code></pre>
<p>This can be useful when you’re reverting more than one commits’
effect to your index in a row.</p>

