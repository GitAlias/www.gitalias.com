<h1 id="more-ideas">More ideas</h1>
<p>For more ideas for git improvements:</p>
<ul>
<li><p>If you want to alias the git command, then use your shell, such
as <code>alias g=git</code>.</p></li>
<li><p>If you want history views, see <a
href="https://github.com/Fakerr/git-recall">git-recall</a></p></li>
<li><p>If you use <code>oh-my-zsh</code>, then you may like the git <a
href="https://github.com/robbyrussell/oh-my-zsh/wiki/Plugin:git">plugin</a></p></li>
<li><p>If you use <code>vim</code> then see <a
href="https://github.com/tpope/vim-fugitive">Fugitive</a></p></li>
<li><p>If you use <code>emacs</code> then see <a
href="https://magit.vc/">Magit</a></p></li>
<li><p>If you use git shell scripting then see <a
href="https://github.com/ndbroadbent/scm_breeze">SCM Breeze</a></p></li>
<li><p>If you use <code>node</code> then see <a
href="https://www.npmjs.com/package/git-alias">git-alias</a></p></li>
</ul>
