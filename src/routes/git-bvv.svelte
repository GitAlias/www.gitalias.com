<h1 id="git-bvv">git bvv</h1>
<h2 id="list-branch-information-with-hash-and-subject-and-upstream">List
branch information with hash and subject and upstream</h2>
<p>Git alias:</p>
<pre class="git"><code>bvv = branch --verbose --verbose</code></pre>
<p>Example:</p>
<pre class="shell"><code>git bvv</code></pre>
<p>When in list mode, then show the hash, the commit subject line, the
upstream branch, etc.</p>
<p>This is identical to doing <code>git b -vv</code>.</p>
